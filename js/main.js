function Show(){
  if (document.getElementById("choosenColor").value == "Blue"){
    document.getElementById("motorchange").style.backgroundColor = "blue";
  }
  else if (document.getElementById("choosenColor").value == "Green"){
    document.getElementById("motorchange").style.backgroundColor = "green";
  }
  else if (document.getElementById("choosenColor").value == "Yellow"){
    document.getElementById("motorchange").style.backgroundColor = "yellow";
  }
  else if (document.getElementById("choosenColor").value == "Red"){
    document.getElementById("motorchange").style.backgroundColor = "red";
  }
  else if (document.getElementById("choosenColor").value == "Violet"){
    document.getElementById("motorchange").style.backgroundColor = "Violet";
  }
  else if (document.getElementById("choosenColor").value == "Maroon"){
    document.getElementById("motorchange").style.backgroundColor = "maroon";  
  }
}

